# Section 1: Python Introduction

# What is Python? Provide a brief introduction to the language.

# Answer: Python is a high-level, interpreted programming language known for its readability and simplicity. It supports multiple programming paradigms, including procedural, object-oriented, and functional programming.

# Section 2: Basic Math Functions

# Perform the following operations:

# a. Addition: 5 + 7
# Answer: 12

# b. Subtraction: 12 - 4
# Answer: 8

# c. Multiplication: 3 * 9
# Answer: 27

# d. Power: 2 ** 4
# Answer: 16

# Section 3: Python Data Types

# Define the following Python data types:

# a. Integer
# Answer: Integers are whole numbers without any decimal points, e.g., 1, -5, 100.

# b. String
# Answer: Strings are sequences of characters, e.g., "hello", 'Python'.

# c. List
# Answer: Lists are ordered collections of items, e.g., [1, 2, 'apple'].

# d. Dictionary
# Answer: Dictionaries are key-value pairs, e.g., {'name': 'John', 'age': 25}.

# Section 4: Intro Data Type Integers and Floats

# Explain the difference between integers and floats. Provide an example for each.

# Answer: Integers are whole numbers (e.g., 5), while floats are numbers with decimal points (e.g., 5.0).

# Section 5: Math Functions

# Using Python, perform the following mathematical operations:

# a. Calculate the square root of 16.
# Answer: import math; math.sqrt(16) or 16 ** 0.5

# b. Calculate the result of dividing 20 by 4.
# Answer: 20 / 4

# Section 6: Variable Intro, Assignment, and Usage

# Describe the concept of variables in Python. Provide an example of variable assignment and usage.

# Answer: Variables in Python are names used to store data values. Example: x = 10. Usage: print(x).

# Section 7: Intro Data Type Strings

# Explain the basics of strings in Python.

# Answer: Strings are sequences of characters enclosed in single or double quotes. Example: "hello".

# Section 8: String Indexing

# Given the string example_string = "PythonCourse", retrieve and print the character at index 3.

# Answer: print(example_string[3]) (Output: h)

# Section 9: String Slicing

# Using slicing, extract and print the substring "thon" from the string example_string.

# Answer: print(example_string[2:6]) (Output: thon)

# Section 10: String Properties and Methods

# Perform the following operations on the string "Hello, World!":

# a. Convert the entire string to uppercase.
# Answer: "Hello, World!".upper()

# b. Convert the entire string to lowercase.
# Answer: "Hello, World!".lower()

# c. Split the string into a list of words.
# Answer: "Hello, World!".split()

# Section 11: String Formatting

# Use the string formatting technique to create a statement that displays the result of the following expression: 10 + 5 = 15.
# Answer: print("{} + {} = {}".format(10, 5, 10 + 5))

# Section 12: Dot Format - Float Format

# Given a floating-point number pi = 3.14159, use the format() method to display it with 2 decimal places.
# Answer: print("Formatted pi: {:.2f}".format(pi))

# Section 13: Intro Data Type Lists

# Provide a brief introduction to lists in Python.
# Answer: Lists are ordered collections that can contain elements of different data types. Example: [1, 'apple', 3.14].

# Section 14: List Methods and Indexing

